-- Consolidated Transfer Trigger (Handles Validation & Balance Updates)
CREATE OR REPLACE TRIGGER TRG_TRANSFER_PROCESS
BEFORE INSERT ON TRANSFERS
FOR EACH ROW
DECLARE
    v_wallet_pin     WALLETS.WALLET_PIN%TYPE;
    v_sender_balance WALLETS.BALANCE%TYPE;
    v_sender_status  WALLETS.WALLET_STATUS%TYPE;
    v_receiver_status WALLETS.WALLET_STATUS%TYPE;
BEGIN
    -- 1. Validate Sender Wallet
    BEGIN
        SELECT WALLET_PIN, BALANCE, WALLET_STATUS 
        INTO v_wallet_pin, v_sender_balance, v_sender_status
        FROM WALLETS WHERE WALLET_ID = :NEW.SENDER_WALLET_ID;
    EXCEPTION WHEN NO_DATA_FOUND THEN
        RAISE_APPLICATION_ERROR(-20001, 'Sender wallet not found.');
    END;

    IF v_sender_status != 'ACTIVE' THEN
        RAISE_APPLICATION_ERROR(-20004, 'Sender wallet is not active.');
    END IF;

    -- 2. Validate Receiver Wallet
    BEGIN
        SELECT WALLET_STATUS INTO v_receiver_status
        FROM WALLETS WHERE WALLET_ID = :NEW.RECEIVER_WALLET_ID;
    EXCEPTION WHEN NO_DATA_FOUND THEN
        RAISE_APPLICATION_ERROR(-20002, 'Receiver wallet not found.');
    END;

    IF v_receiver_status != 'ACTIVE' THEN
        RAISE_APPLICATION_ERROR(-20005, 'Receiver wallet is not active.');
    END IF;

    -- 3. PIN Validation
    IF :NEW.PIN = 'VERIFIED_BY_API' THEN
        NULL; -- Skip if already verified by backend
    ELSIF :NEW.PIN IS NULL OR :NEW.PIN != v_wallet_pin THEN
        RAISE_APPLICATION_ERROR(-20003, 'Security Error: Invalid or Missing PIN.');
    END IF;

    -- 4. Balance Validation
    IF v_sender_balance < :NEW.AMOUNT THEN
        RAISE_APPLICATION_ERROR(-20006, 'Insufficient Balance for transfer.');
    END IF;

    -- 5. Update Balances
    UPDATE WALLETS SET BALANCE = BALANCE - :NEW.AMOUNT WHERE WALLET_ID = :NEW.SENDER_WALLET_ID;
    UPDATE WALLETS SET BALANCE = BALANCE + :NEW.AMOUNT WHERE WALLET_ID = :NEW.RECEIVER_WALLET_ID;
    
    -- 6. Finalize Transfer Record
    :NEW.STATUS := 'COMPLETED';
    :NEW.TRANSFER_DATE := SYSTIMESTAMP;
END;
/

CREATE OR REPLACE TRIGGER TRG_CARD_REQUEST_FEE
BEFORE INSERT ON CARD_REQUESTS
FOR EACH ROW
DECLARE
    v_wallet_id NUMBER;
BEGIN
    SELECT WALLET_ID INTO v_wallet_id FROM WALLETS WHERE USER_ID = :NEW.USER_ID;
    :NEW.STATUS := 'PENDING';
EXCEPTION
    WHEN NO_DATA_FOUND THEN
        RAISE_APPLICATION_ERROR(-20003, 'User wallet not found');
END;
/

CREATE OR REPLACE TRIGGER TRG_CARD_AUTO_EXPIRE
BEFORE UPDATE ON CARDS
FOR EACH ROW
BEGIN
    IF :NEW.EXPIRY_DATE < SYSDATE AND :NEW.CARD_STATUS = 'ACTIVE' THEN
        :NEW.CARD_STATUS := 'EXPIRED';
    END IF;
END;
/

CREATE OR REPLACE TRIGGER TRG_USERS_UPDATE BEFORE UPDATE ON USERS FOR EACH ROW BEGIN :NEW.UPDATED_AT := SYSTIMESTAMP; END;
/
CREATE OR REPLACE TRIGGER TRG_WALLETS_UPDATE BEFORE UPDATE ON WALLETS FOR EACH ROW BEGIN :NEW.UPDATED_AT := SYSTIMESTAMP; END;
/
CREATE OR REPLACE TRIGGER TRG_CARDS_UPDATE BEFORE UPDATE ON CARDS FOR EACH ROW BEGIN :NEW.UPDATED_AT := SYSTIMESTAMP; END;
/

