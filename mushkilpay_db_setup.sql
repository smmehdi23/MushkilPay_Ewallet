CREATE OR REPLACE VIEW USER_WALLET_VIEW AS
SELECT 
    W.WALLET_ID, W.USER_ID, U.FULL_NAME, U.EMAIL, W.ACCOUNT_NUMBER,
    W.BALANCE, W.WALLET_STATUS, W.CREATED_AT
FROM WALLETS W
JOIN USERS U ON W.USER_ID = U.USER_ID;

CREATE OR REPLACE VIEW USER_TRANSFER_HISTORY AS
SELECT 
    T.TRANSFER_ID, T.SENDER_WALLET_ID, T.RECEIVER_WALLET_ID, T.AMOUNT,
    T.TRANSFER_DATE, T.STATUS, SU.FULL_NAME AS SENDER_NAME, RU.FULL_NAME AS RECEIVER_NAME
FROM TRANSFERS T
LEFT JOIN WALLETS SW ON SW.WALLET_ID = T.SENDER_WALLET_ID
LEFT JOIN USERS SU ON SU.USER_ID = SW.USER_ID
LEFT JOIN WALLETS RW ON RW.WALLET_ID = T.RECEIVER_WALLET_ID
LEFT JOIN USERS RU ON RU.USER_ID = RW.USER_ID
ORDER BY T.TRANSFER_DATE DESC;

CREATE OR REPLACE VIEW ADMIN_DASHBOARD_SUMMARY AS
SELECT 
    (SELECT COUNT(*) FROM USERS) AS total_users,
    (SELECT COUNT(*) FROM WALLETS) AS total_wallets,
    (SELECT COUNT(*) FROM CARDS) AS total_cards,
    (SELECT SUM(BALANCE) FROM WALLETS) AS system_balance,
    (SELECT COUNT(*) FROM WALLETS WHERE WALLET_STATUS = 'ACTIVE') AS active_wallets,
    (SELECT COUNT(*) FROM TRANSFERS) AS total_transfers,
    (SELECT COUNT(*) FROM NOTIFICATIONS WHERE IS_READ = 0) AS unread_notifications
FROM DUAL;

