-- ============================================================================
-- 1. TABLES
-- ============================================================================

CREATE TABLE USERS (
    USER_ID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    FULL_NAME VARCHAR2(100) NOT NULL,
    EMAIL VARCHAR2(100) UNIQUE NOT NULL,
    PHONE VARCHAR2(20),
    PASSWORD_HASH VARCHAR2(255) NOT NULL,
    CNIC VARCHAR2(20),
    STATUS VARCHAR2(20) DEFAULT 'ACTIVE' CHECK (STATUS IN ('ACTIVE','BLOCKED')),
    CREATED_AT TIMESTAMP DEFAULT SYSTIMESTAMP,
    UPDATED_AT TIMESTAMP DEFAULT SYSTIMESTAMP,
    PRIMARY KEY (USER_ID)
);

CREATE TABLE WALLETS (
    WALLET_ID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    USER_ID NUMBER NOT NULL UNIQUE,
    ACCOUNT_NUMBER VARCHAR2(30) UNIQUE,
    BALANCE NUMBER(12, 2) DEFAULT 0.00,
    WALLET_PIN VARCHAR2(255),
    WALLET_STATUS VARCHAR2(20) DEFAULT 'ACTIVE',
    CREATED_AT TIMESTAMP DEFAULT SYSTIMESTAMP,
    UPDATED_AT TIMESTAMP DEFAULT SYSTIMESTAMP,
    PRIMARY KEY (WALLET_ID),
    FOREIGN KEY (USER_ID) REFERENCES USERS(USER_ID) ON DELETE CASCADE
);

CREATE TABLE CARDS (
    CARD_ID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    USER_ID NUMBER NOT NULL,
    CARD_NUMBER VARCHAR2(20) UNIQUE NOT NULL,
    CVV VARCHAR2(10) NOT NULL,
    EXPIRY_DATE DATE,
    CARD_STATUS VARCHAR2(20) DEFAULT 'INACTIVE',
    CARD_TYPE VARCHAR2(20) DEFAULT 'DEBIT',
    CREATED_AT TIMESTAMP DEFAULT SYSTIMESTAMP,
    UPDATED_AT TIMESTAMP DEFAULT SYSTIMESTAMP,
    PRIMARY KEY (CARD_ID),
    FOREIGN KEY (USER_ID) REFERENCES USERS(USER_ID) ON DELETE CASCADE
);

CREATE TABLE TRANSFERS (
    TRANSFER_ID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    SENDER_WALLET_ID NUMBER NOT NULL,
    RECEIVER_WALLET_ID NUMBER NOT NULL,
    AMOUNT NUMBER(12, 2) NOT NULL,
    PIN VARCHAR2(255),
    TRANSFER_DATE TIMESTAMP DEFAULT SYSTIMESTAMP,
    STATUS VARCHAR2(20) DEFAULT 'PENDING',
    PRIMARY KEY (TRANSFER_ID),
    FOREIGN KEY (SENDER_WALLET_ID) REFERENCES WALLETS(WALLET_ID),
    FOREIGN KEY (RECEIVER_WALLET_ID) REFERENCES WALLETS(WALLET_ID)
);

CREATE TABLE ADMINS (
    ADMIN_ID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    FULL_NAME VARCHAR2(100) NOT NULL,
    EMAIL VARCHAR2(100) UNIQUE NOT NULL,
    PHONE VARCHAR2(20) UNIQUE,
    USERNAME VARCHAR2(50) UNIQUE NOT NULL,
    PASSWORD_HASH VARCHAR2(255) NOT NULL,
    ROLE VARCHAR2(50) DEFAULT 'admin',
    STATUS VARCHAR2(20) DEFAULT 'ACTIVE' CHECK (STATUS IN ('ACTIVE','BLOCKED')),
    CREATED_AT TIMESTAMP DEFAULT SYSTIMESTAMP,
    PRIMARY KEY (ADMIN_ID)
);

CREATE TABLE ADMIN_USER (
    ADMIN_ID NUMBER NOT NULL,
    USER_ID NUMBER NOT NULL,
    PRIMARY KEY (ADMIN_ID, USER_ID),
    FOREIGN KEY (ADMIN_ID) REFERENCES ADMINS(ADMIN_ID) ON DELETE CASCADE,
    FOREIGN KEY (USER_ID) REFERENCES USERS(USER_ID) ON DELETE CASCADE
);

CREATE TABLE BANK_ACCOUNTS (
    BANK_ACCOUNT_ID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    USER_ID NUMBER UNIQUE NOT NULL,
    BANK_NAME VARCHAR2(100),
    ACCOUNT_NUMBER VARCHAR2(50) UNIQUE NOT NULL,
    ACCOUNT_TYPE VARCHAR2(30),
    LINKED_DATE TIMESTAMP DEFAULT SYSTIMESTAMP,
    PRIMARY KEY (BANK_ACCOUNT_ID),
    FOREIGN KEY (USER_ID) REFERENCES USERS(USER_ID) ON DELETE CASCADE
);

CREATE TABLE TRANSACTIONS (
    TRANSACTION_ID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    WALLET_ID NUMBER NOT NULL,
    TRANSACTION_TYPE VARCHAR2(30) CHECK (TRANSACTION_TYPE IN ('DEPOSIT','WITHDRAWAL','TRANSFER')),
    AMOUNT NUMBER(12, 2) CHECK (AMOUNT > 0),
    TRANSACTION_DATE TIMESTAMP DEFAULT SYSTIMESTAMP,
    STATUS VARCHAR2(20) CHECK (STATUS IN ('SUCCESS','FAILED','PENDING','COMPLETED')),
    REFERENCE_ID VARCHAR2(50),
    PRIMARY KEY (TRANSACTION_ID),
    FOREIGN KEY (WALLET_ID) REFERENCES WALLETS(WALLET_ID) ON DELETE CASCADE
);

CREATE TABLE NOTIFICATIONS (
    NOTIF_ID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    USER_ID NUMBER NOT NULL,
    TITLE VARCHAR2(80),
    MESSAGE VARCHAR2(400),
    CREATED_AT TIMESTAMP DEFAULT SYSTIMESTAMP,
    IS_READ NUMBER(1) DEFAULT 0,
    PRIMARY KEY (NOTIF_ID),
    FOREIGN KEY (USER_ID) REFERENCES USERS(USER_ID) ON DELETE CASCADE
);

CREATE TABLE CARD_REQUESTS (
    REQUEST_ID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    USER_ID NUMBER NOT NULL,
    REQUEST_TYPE VARCHAR2(20),
    REASON VARCHAR2(50),
    DELIVERY_ADDRESS VARCHAR2(300),
    STATUS VARCHAR2(20) DEFAULT 'PENDING',
    CHARGE_AMOUNT NUMBER(12, 2) DEFAULT 0,
    CREATED_AT TIMESTAMP DEFAULT SYSTIMESTAMP,
    PRIMARY KEY (REQUEST_ID),
    FOREIGN KEY (USER_ID) REFERENCES USERS(USER_ID) ON DELETE CASCADE
);

CREATE TABLE CARD_PINS (
    CARD_ID NUMBER PRIMARY KEY,
    PIN_HASH VARCHAR2(200),
    UPDATED_AT TIMESTAMP DEFAULT SYSTIMESTAMP,
    FOREIGN KEY (CARD_ID) REFERENCES CARDS(CARD_ID) ON DELETE CASCADE
);

CREATE TABLE WITHDRAWAL_LIMITS (
    USER_ID NUMBER PRIMARY KEY,
    LIMIT_AMOUNT NUMBER(12, 2),
    UPDATED_AT TIMESTAMP DEFAULT SYSTIMESTAMP,
    FOREIGN KEY (USER_ID) REFERENCES USERS(USER_ID) ON DELETE CASCADE
);

CREATE TABLE TOPUP_REQUESTS (
    TOPUP_ID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    ADMIN_USERNAME VARCHAR2(80),
    USER_ID NUMBER NOT NULL,
    ACCOUNT_NUMBER VARCHAR2(30),
    AMOUNT NUMBER(12, 2) NOT NULL,
    METHOD VARCHAR2(20),
    REFERENCE VARCHAR2(120),
    STATUS VARCHAR2(20) DEFAULT 'COMPLETED',
    CREATED_AT TIMESTAMP DEFAULT SYSTIMESTAMP,
    PRIMARY KEY (TOPUP_ID),
    FOREIGN KEY (USER_ID) REFERENCES USERS(USER_ID) ON DELETE CASCADE
);

CREATE TABLE ADMIN_SETTINGS (
    ADMIN_ID NUMBER PRIMARY KEY,
    SESSION_TIMEOUT NUMBER DEFAULT 3600,
    TWO_FA_ENABLED NUMBER(1) DEFAULT 0,
    NOTIFICATION_EMAILS NUMBER(1) DEFAULT 1,
    MAX_TOPUP_PER_TRANSACTION NUMBER(12, 2) DEFAULT 1000000,
    MAX_TOPUP_PER_DAY NUMBER(12, 2) DEFAULT 10000000,
    WITHDRAWAL_LIMIT NUMBER(12, 2) DEFAULT 500000,
    AUDIT_LOGS_RETENTION_DAYS NUMBER DEFAULT 90,
    UPDATED_AT TIMESTAMP DEFAULT SYSTIMESTAMP,
    FOREIGN KEY (ADMIN_ID) REFERENCES ADMINS(ADMIN_ID) ON DELETE CASCADE
);

